# 1. if Statement
x = 10
if x > 5:
    print("x is greater than 5")

# 2. if-else Statement
num = 3
if num % 2 == 0:
    print("Even")
else:
    print("Odd")

# 3. if-elif-else Statement
score = 85
if score >= 90:
    print("A")
elif score >= 80:
    print("B")
else:
    print("C or below")

# 4. Ternary Operator
age = 20
status = "Adult" if age >= 18 else "Minor"
print(status)
